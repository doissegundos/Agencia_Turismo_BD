INNER JOIN CIDADE C ON (C.CODIGO_CIDADE=E.ID_CIDADE)
INNER JOIN PONTO_TURISTICO PT ON (PT.ID_ENDERECO=E.CODIGO_ENDERECO)
INNER JOIN MUSEU M ON (M.CODIGO_MUSEU=PT.CODIGO_PONTO_TURISTICO) WHERE C.POPULACAO>20000 AND C.POPULACAO<100000

SELECT C.NOME AS [CIDADE] FROM ENDERECO E
