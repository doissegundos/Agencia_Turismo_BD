INNER JOIN MUSEU M ON (PT.CODIGO_PONTO_TURISTICO=M.CODIGO_MUSEU)
INNER JOIN ENDERECO E ON (E.CODIGO_ENDERECO=PT.ID_ENDERECO)
INNER JOIN CIDADE C ON (C.CODIGO_CIDADE= E.ID_CIDADE)
INNER JOIN FUNDADOR F ON (F.CODIGO_FUNDADOR = M.ID_FUNDADOR) WHERE F.NACIONALIDADE='NACIONALIDADE 03'

SELECT F.NOME [NOME FUNDADOR], E.RUA, C.NOME AS [NOME CIDADE] FROM PONTO_TURISTICO PT
INNER JOIN IGREJA I ON (PT.CODIGO_PONTO_TURISTICO=I.CODIGO_IGREJA)
INNER JOIN ENDERECO E ON (E.CODIGO_ENDERECO=PT.ID_ENDERECO)
INNER JOIN CIDADE C ON (C.CODIGO_CIDADE= E.ID_CIDADE)
INNER JOIN FUNDADOR F ON (F.CODIGO_FUNDADOR = I.ID_FUNDADOR) WHERE F.NACIONALIDADE<>'NACIONALIDADE 00'

