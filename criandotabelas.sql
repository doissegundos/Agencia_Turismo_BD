CREATE TABLE [PONTO_TURISTICO](
	[CODIGO_PONTO_TURISTICO] INT IDENTITY(1,1) NOT NULL,
	[ID_CIDADE] INT NOT NULL,
	[ID_ENDERECO] INT NOT NULL,
	[TELEFONE] VARCHAR(50) NOT NULL,
	[DESCRICAO] VARCHAR(50) NOT NULL,
	[TIPO_PONTO_TURISTICO] VARCHAR(50) NOT NULL,
	CONSTRAINT [PK_PONTO_TURISTICO] PRIMARY KEY CLUSTERED ([CODIGO_PONTO_TURISTICO]),
	CONSTRAINT [FK_PONTO_TURISTICO_CIDADE] FOREIGN KEY ([ID_CIDADE]) REFERENCES [CIDADE] ([CODIGO_CIDADE]),
	CONSTRAINT [FK_PONTO_TURISTICO_ENDERECO] FOREIGN KEY ([ID_ENDERECO]) REFERENCES [ENDERECO] ([CODIGO_ENDERECO]),
);

CREATE TABLE [FUNDADOR](
	[CODIGO_FUNDADOR] INT IDENTITY(1,1) NOT NULL,
