	[NACIONALIDADE] VARCHAR(50) NOT NULL,
	[PROFISSAO] VARCHAR(50) NOT NULL,
	[DATA_NASCIMENTO] DATE NOT NULL,
	[DATA_MORTE] DATE NOT NULL,
