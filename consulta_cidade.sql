--1.1- Quais os pontos turísticos e seus endereços;
SELECT [TIPO_PONTO_TURISTICO], [ID_ENDERECO],E.RUA,E.NUMERO,E.BAIRRO,E.CEP,C.NOME,C.ESTADO FROM [PONTO_TURISTICO] PT
