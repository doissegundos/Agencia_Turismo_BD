INNER JOIN [CIDADE] C ON C.CODIGO_CIDADE=PT.[ID_CIDADE] WHERE  C.NOME ='CIDADE 00' 


--1.2- Quais as igrejas e museus com fundadores nacionais;
SELECT * FROM FUNDADOR
SELECT * FROM MUSEU
SELECT * FROM PONTO_TURISTICO

SELECT [TIPO_PONTO_TURISTICO], I.NOME AS IGREJA, F.NOME AS FUNDADOR FROM [PONTO_TURISTICO] PT
INNER JOIN IGREJA I ON (I.CODIGO_IGREJA=PT.CODIGO_PONTO_TURISTICO)
INNER JOIN FUNDADOR F ON (F.CODIGO_FUNDADOR=I.ID_FUNDADOR)
INNER JOIN [CIDADE] C ON C.CODIGO_CIDADE=PT.[ID_CIDADE] WHERE  F.NACIONALIDADE='NACIONALIDADE 03' 

SELECT [TIPO_PONTO_TURISTICO], M.NOME AS MUSEU, F.NOME AS FUNDADOR FROM [PONTO_TURISTICO] PT
INNER JOIN MUSEU M ON (M.CODIGO_MUSEU=PT.CODIGO_PONTO_TURISTICO)
INNER JOIN FUNDADOR F ON (F.CODIGO_FUNDADOR=M.ID_FUNDADOR)
INNER JOIN [CIDADE] C ON C.CODIGO_CIDADE=PT.[ID_CIDADE] WHERE  F.NACIONALIDADE='NACIONALIDADE 03' AND C.NOME = 'CIDADE 03' 

